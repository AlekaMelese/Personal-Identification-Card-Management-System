<center><img width="800" height="500" src="images/KEB.jpg">
A kebele is the smallest administrative unit of Ethiopia, similar to a ward, a neighbourhood or a localized and delimited group of people. It is part of a woreda (district), itself usually part of a Zone, which in turn are grouped into one of the Regions based on ethno-linguistic communities (or kililoch) that comprise the Federal Democratic Republic of Ethiopia. Each kebele consists of at least five hundred families, or the equivalent of 3,500 to 4,000 persons. There is at least one in every town with more than 2,000 population. A representative, had jurisdiction over six to twelve kebeles.

The kebele, also referred to as a peasant association, was created by the Derg in 1975 to promote development and to manage land reform; they became a key element that the rival Ethiopian People's Revolutionary Party and MEISON fought each other, and the ruling Derg, to control during the Ethiopian Red Terror. These armed members, formed into neighborhood defense squads, were responsible for many of the brutal excesses of the Red Terror.

Upon the Ethiopian People's Revolutionary Democratic Front assuming power at the end of the Ethiopian Civil War in 1991, it retained the peasant associations, or kebeles, but exploited their function for providing services such as healthcare through the Health Extension Workers program. As Human Rights Watch noted, "Kebele officials determine eligibility for food assistance, recommend referrals to secondary health care and schools, and help provide access to state-distributed resources such as seeds, fertilizers, credit, and other essential agricultural inputs."
